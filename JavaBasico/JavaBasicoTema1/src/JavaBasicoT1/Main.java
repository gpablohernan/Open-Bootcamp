package JavaBasicoT1;

public class Main {
    public static void main(String[] args) {

        short corto = 3;
        long largo = 500;
        int entero = 10;
        float flotante = 10.5f;
        double doble = 19.3d;
        char caracter = 'a';
        String cadena = "Hola";
        boolean booleano = true;

        System.out.println("short: " + corto);
        System.out.println("long: " + largo);
        System.out.println("int: " + entero);
        System.out.println("float:" + flotante);
        System.out.println("double: " + doble);
        System.out.println("char: " + caracter);
        System.out.println("String: " + cadena);
        System.out.println("boolean: " + booleano);
    }
}
